                                        <h1>人事代理</h1>
                    <ul>
                        <!-- <li>作者：为力小编</li> -->
                        <li>发布时间：2018-09-28 10:56:40</li>
                        <li>浏览次数：157</li>
                    </ul>
                </div>

                <div class="content_con_type">
                    <p>
                        <span style="font-size:14px;"><strong>人事代理</strong>，是一种人力资源外包形式，是企业根据需求将一项或多项人力资源管理工作或职能外包出去，由专业的第三方代理。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;">人事代理是与社会主义市场经济体制相配套的新型人事管理模式，是指政府人事部门所属人才服务机构受单位或个人委托，运用社会化服务方式和现代科技手段，按照一定的法律程序和政策规定，代办有关人事业务。</span> 
                    </p>
                    <p style="text-align:center;">
                        <span style="font-size:14px;"><img src="__PICTURE__/201809281056351460200334.jpg" alt="" /><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><strong>人事代理服务内容：</strong></span> 
                    </p>
                    <p>
                        <span style="font-size:14px;">1、政策咨询与规划。向委托代理单位提供国家人事工作方面的法律、法规和政策规定的咨询服务；协助委托单位进行人事规划设计，建立新型人事管理制度；帮助委托单位解决人事工作中的问题。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
2、人才招聘引进。根据委托单位工作和发展对人才的具体要求，代拟和代发人才招聘启事，组织报名、考试、考核。素质测评工作，提出初选名单。根据单位的特殊需求，向省内外、国外招聘引进人才。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
3、应届毕业生人事代理。为委托单位提供应届毕业生就业政策咨询，申报应届大<br />
中专毕业生需求计划，为接收的大中专毕业生按转档案，办理转正定级手续。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
4、代办专业技术职称申报评审手续。为委托单位专业技术人员和管理人员办理专业技术职务资格初走及资格考试报名、晋升推荐手续，组建相应评审委员会，负责对部分专业技术职务资格评审与评议推荐工作。<br />
5、人事档案管理。按照有关政策规定，管理委托单位专业技术人员、管理人员的人事档案、考绩档案，为委托代理人员保留原有身份、计算工龄、调整档案工资、接转党团员组织关系、办理出国（境）政审手续、代办集体户口落户、出具以档案材料为依据的有关证明。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
6、办理在职流动人员人事关系按转手续。根据单位工作需要，为聘用人员接转人事关系、党团员组织关系等；为合同期满流动者，办理人事关系转出手续。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
7、聘用合同鉴证。按照有关规定，办理聘用专业技术人员、管理人员的合同鉴证。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
8、 协调专业技术人员流动争议。受人才流动争议仲裁机构的委托，受理专业技术人员、管理人员流动争议申请，进行调查、取证，根据有关规定进行凋解或提交仲裁。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
9、 开展岗位及专业技能培训。根据单位对人员素质和技能的要求，进行岗位知识及专业技能培训，为单位代培中。长期专业人才。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
10、 按照有关协议，向社会推荐委托单位的辞聘、解聘人员重新就业。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
11、 代办失业、养老保险。按照国家和我省有关规定，为委托单位代管人员代办失业、养老保险。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
12、 为委托单位承办其他人事管理事宜。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
13、对个人委托的人事代理，可参照上述人事代理内容提供代理服务。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><strong>服务对象：</strong></span> 
                    </p>
                    <p>
                        <span style="font-size:14px;">（一）各类非国有（三资、集体、民营、乡企、私营等）单位和其他自愿委托的各类国有企事业单位。<br />
（二）下列专业技术人员和管理人员（即各类流动人员）应 该进行人事代理。<br />
（1）辞职、辞退和解除聘用合同的人员；<br />
（2）各类事务所和其他社会中介组织聘用的人员；<br />
（3）各类非国有单位聘用的人员；<br />
（4）自费大中专毕业和毕业研究生；<br />
（5）自谋职业的军队转业干部；<br />
（6）自费出国留学人员和留学回国人员；<br />
（7）其他各类流动人员。<br />
<br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><strong>服务流程：</strong></span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> </span> 
                    </p>
                    <p>
                        <br />
                    </p>
                    <p>
                        <span style="font-size:14px;">1、与客户企业沟通服务需求和要求。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;">2、双方企业交流相关情况，并提供各自营业执照、机构代码证及企业概况等相关经营合法资料。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> 3、根据客户企业的相关需求和要求，制定服务方案，并与客户企业作相关沟通</span> 
                    </p>
                    <p>
                        <img src="file://https://gss1.bdstatic.com/-vo3dsag_xi4khgkpowk1hf6hhy/baike/s=220/sign=6fdd8bef1e178a82ca3c78a2c602737f/908fa0ec08fa513dcb6c1dfd3d6d55fbb3fbd9cb.jpg" /><span style="font-size:14px;">人事代理所。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> 4、双方沟通达成一致意见，并约定具体服务事项、服务内容、双方的权利及义务、解除及终止协议情形等相关条款，签订《人事服务合同》。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> 5、人事代理机构提供专业对口服务人员，由需客户企业确定联系人配合，就相关服务事务合同规定开展服务工作。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> 6、需由人事代理机构代为签订员工《劳动合同》的，客户企业还需向人事代理机构提供客户企业一方相关的劳动人事规章制度和员工手册，以及员工的相关工作岗位、薪酬、福利、奖金及其一系列必要的雇用信息。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"> 7、委托人事代理机构进行员工《劳动合同》解除、终止或续签等合同管理的，人事代理机构将负责提供与此相关的法律咨询，并代为进行相关程序办理。</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;"><br />
</span> 
                    </p>
                    <span style="font-size:14px;"> 8、人事代理机构负责管理客户企业雇用员工的信息，建立雇佣员工个人信息档案并作即时的更新。&nbsp;</span> 
                    <p>
