                        <span style="font-size:14px;">1.社保开户，公积金开户；</span><span style="font-size:14px;">社保</span>
                        <br />
                        <span style="font-size:14px;"> 2.社保账户的增减员，五险一金的缴纳；</span>
                        <br />
                        <span style="font-size:14px;"> 3.生育报销、工伤保险报销、公积金提取等业务；</span>
                        <br />
                        <span style="font-size:14px;"> 4.解答五险一金相关问题；</span> 
                    </p>
                    <p>
                        <span style="font-size:14px;">5.其他五险一金相关的业务办理。&nbsp;</span> 
                    </p>
                    <p>
                        <strong><span style="font-size:14px;">服务对象：</span></strong> 
                    </p>
                    <p>
                        <span style="font-size:14px;">（1）外资企业和异地企业驻本地分支机构；</span>
                        <br />
                        <span style="font-size:14px;"> （2）各商业连锁加盟企业常驻经营网点；</span>
                        <br />
                        <span style="font-size:14px;"> （3）不设人事管理岗位的中小企业；</span>
                        <br />
                        <span style="font-size:14px;"> （4）没有企业为其缴纳社保的个体工商户；</span>
                        <br />
                        <span style="font-size:14px;"> （5）自由职业者；</span>
                        <br />
                        <span style="font-size:14px;"> （6）因工作变动，暂时没有公司为其缴纳社保的人员；</span>
                        <br />
                        <span style="font-size:14px;"> （7）其他符合政策规定的单位和个人。</span> 
                    </p>
                    <p>
                        <br />
                    </p>
                    <strong>代交社保的好处：</strong> 
                    <p>
                        <span><span style="font-size:14px;">简答来说，代缴社保对企业的帮助是：提高效率，规避风险，节省成本，促进发展。</span>
                        <br />
                        <span style="font-size:14px;">具体而言：</span>
                        <br />
                        <span style="font-size:14px;">1、帮助人力资源部门从繁重的重复性事务中解脱，专注于核心的战略性工作，从而提升人力资源管理的高度和核心竞争力；</span>
                        <br />
                        <span style="font-size:14px;">2、提供接触新管理技术的机会，提高响应的速度与效率；</span>
                        <br />
                        <span style="font-size:14px;">3、规范操作，有效遏制随意性的薪资、员工管理，对管理工作的规范性、公正性起到促进作用；</span>
                        <br />
                        <span style="font-size:14px;">4、降低成本，舒缓资金压力，克服企业很多的规模经济弱点，在国内由于劳工权利意识的提高、就业安全体系和劳动法令的普及，人事直接间接费用及外围成本不断地增加，人力资源管理业务外包可以降低企业风险，摆脱杂务干扰，最终引导企业专心经营核心资源，发展核心竞争优势；</span>
                        <br />
