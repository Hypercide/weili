                <div class="content_con_type">
                    <p>
                        <span style="font-size:14px;">代缴社保是社保代理服务的主要内容，指代理公司为企业或个人提供代办或缴纳社会保险的服务，是人事代理服务中众多项目之一。</span> 
                    </p>
                    <p>
                        <br />
                    </p>
                    <p>
                        <span style="font-size:14px;">代缴社保作为一种中介服务，随着各地相关配套政策落实到位，越来越受到社会关注，并已成为一种产业。</span> 
                    </p>
                    <p>
                        <br />
                    </p>
                    <p>
                        <span style="font-size:14px;"><strong>社会保险主要包括五种：</strong><br />
1、养老保险：即劳动者因年老丧失劳动能力时，在养老期间发给生活费，以及生活方面给以照顾。<br />
2、医疗保险：即劳动者在患病期间在医疗、护理方面的保障措施。<br />
3、失业保险：即劳动者在失业期间的生活费、医疗费的给付以及转业培训、生产自救及 职业介绍等保障措施。<br />
4、工伤保险：即劳动者因工负伤，暂时或永久丧失劳动能力后的工资收入补偿，也是对因工负伤劳动者的医疗护理和生活照顾措施。<br />
5、生育保险：女职工在生育期间保障，以及家属可以享受的待遇。<br />
</span> 
                    </p>
                    <div style="text-align:center;">
                        <img src="__PICTURE__/201809281221111146335447.jpg" alt="" />
                    </div>
                    <p>
                        <br />
                    </p>
                    <p>
                        <span style="font-size:14px;"><strong>服务内容：</strong></span> 
                    </p>
                    <p>
